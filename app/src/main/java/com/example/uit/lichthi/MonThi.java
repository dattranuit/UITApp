package com.example.uit.lichthi;

public class MonThi {


    String stt, maMon, maLop, caThi, thuThi, ngayThi, phongThi, tenMonThi;

    public MonThi(String stt, String maMon, String maLop, String caThi, String thuThi, String ngayThi, String phongThi, String tenMonThi) {
        this.stt = stt;
        this.maMon = maMon;
        this.maLop = maLop;
        this.caThi = caThi;
        this.thuThi = thuThi;
        this.ngayThi = ngayThi;
        this.phongThi = phongThi;
        this.tenMonThi = tenMonThi;
    }


    public String getTenMonThi() {
        return tenMonThi;
    }

    public void setTenMonThi(String tenMonThi) {
        this.tenMonThi = tenMonThi;
    }

    public String getStt() {
        return stt;
    }

    public void setStt(String stt) {
        this.stt = stt;
    }

    public String getMaMon() {
        return maMon;
    }

    public void setMaMon(String maMon) {
        this.maMon = maMon;
    }

    public String getMaLop() {
        return maLop;
    }

    public void setMaLop(String maLop) {
        this.maLop = maLop;
    }

    public String getCaThi() {
        return caThi;
    }

    public void setCaThi(String caThi) {
        this.caThi = caThi;
    }

    public String getThuThi() {
        return thuThi;
    }

    public void setThuThi(String thuThi) {
        this.thuThi = thuThi;
    }

    public String getNgayThi() {
        return ngayThi;
    }

    public void setNgayThi(String ngayThi) {
        this.ngayThi = ngayThi;
    }

    public String getPhongThi() {
        return phongThi;
    }

    public void setPhongThi(String phongThi) {
        this.phongThi = phongThi;
    }





}
