package com.example.uit.lichhoc;

public class MonHoc {

    String tenMonHoc, maLop, thoiGianBatDau, thoiGianKetThu, hinhThuc, hocThu, phongHoc, tenGiangVien, thongTin;

    public MonHoc(String tenMonHoc, String maLop, String thoiGianBatDau, String thoiGianKetThu, String hinhThuc, String hocThu, String phongHoc, String tenGiangVien, String thongTin) {
        this.tenMonHoc = tenMonHoc;
        this.maLop = maLop;
        this.thoiGianBatDau = thoiGianBatDau;
        this.thoiGianKetThu = thoiGianKetThu;
        this.hinhThuc = hinhThuc;
        this.hocThu = hocThu;
        this.phongHoc = phongHoc;
        this.tenGiangVien = tenGiangVien;
        this.thongTin = thongTin;
    }

    public String getThongTin() {
        return thongTin;
    }

    public void setThongTin(String thongTin) {
        this.thongTin = thongTin;
    }

    public String getTenMonHoc() {
        return tenMonHoc;
    }

    public void setTenMonHoc(String tenMonHoc) {
        this.tenMonHoc = tenMonHoc;
    }

    public String getMaLop() {
        return maLop;
    }

    public void setMaLop(String maLop) {
        this.maLop = maLop;
    }

    public String getThoiGianBatDau() {
        return thoiGianBatDau;
    }

    public void setThoiGianBatDau(String thoiGianBatDau) {
        this.thoiGianBatDau = thoiGianBatDau;
    }

    public String getThoiGianKetThu() {
        return thoiGianKetThu;
    }

    public void setThoiGianKetThu(String thoiGianKetThu) {
        this.thoiGianKetThu = thoiGianKetThu;
    }

    public String getHinhThuc() {
        return hinhThuc;
    }

    public void setHinhThuc(String hinhThuc) {
        this.hinhThuc = hinhThuc;
    }

    public String getHocThu() {
        return hocThu;
    }

    public void setHocThu(String hocThu) {
        this.hocThu = hocThu;
    }

    public String getPhongHoc() {
        return phongHoc;
    }

    public void setPhongHoc(String phongHoc) {
        this.phongHoc = phongHoc;
    }

    public String getTenGiangVien() {
        return tenGiangVien;
    }

    public void setTenGiangVien(String tenGiangVien) {
        this.tenGiangVien = tenGiangVien;
    }
}
