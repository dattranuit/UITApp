package com.example.uit.diem;

public class DiemThi {
    String maMonHoc;
    String tenMonHoc;
    String tC, qT,tH,gK,cK,tB;

    public DiemThi(String maMonHoc, String tC, String qT, String tH, String gK, String cK, String tB) {
        this.maMonHoc = maMonHoc;
        this.tC = tC;
        this.qT = qT;
        this.tH = tH;
        this.gK = gK;
        this.cK = cK;
        this.tB = tB;
    }

    public String getMaMonHoc() {
        return maMonHoc;
    }

    public void setMaMonHoc(String maMonHoc) {
        this.maMonHoc = maMonHoc;
    }

    public String getTenMonHoc() {
        return tenMonHoc;
    }

    public void setTenMonHoc(String tenMonHoc) {
        this.tenMonHoc = tenMonHoc;
    }

    public String gettC() {
        return tC;
    }

    public void settC(String tC) {
        this.tC = tC;
    }

    public String getqT() {
        return qT;
    }

    public void setqT(String qT) {
        this.qT = qT;
    }

    public String gettH() {
        return tH;
    }

    public void settH(String tH) {
        this.tH = tH;
    }

    public String getgK() {
        return gK;
    }

    public void setgK(String gK) {
        this.gK = gK;
    }

    public String getcK() {
        return cK;
    }

    public void setcK(String cK) {
        this.cK = cK;
    }

    public String gettB() {
        return tB;
    }

    public void settB(String tB) {
        this.tB = tB;
    }





}
